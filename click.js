function clicked(classClicked)
{
    switch(classClicked)
    {
        case 'index':
            window.location.href = 'index.html';
            break;
        case 'entrance':
            window.location.href = 'entrance.html';
            break;
        case 'video':
            window.location.href = 'video.html';
            break;
        case 'art':
            window.location.href = 'art.html';
            break;
        case 'notebook':
            window.location.href = 'notebook.html';
            break;
    }
}